    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        
        FuncionarioDTO funcionarioDTO = new FuncionarioDTO();
        FuncionarioCTR funcionarioCTR = new FuncionarioCTR();
        
        funcionarioDTO.setNome(txtNome.getText());
        funcionarioDTO.setLogadouro(txtLogadouro.getText());
        funcionarioDTO.setNumero(Integer.parseInt(txtNumero.getText()));
        funcionarioDTO.setBairro(txtBairro.getText());
        funcionarioDTO.setCidade(txtCidade.getText());
        funcionarioDTO.setEstado(txtEstado.getText());
        funcionarioDTO.setCep(txtCep.getText());
        funcionarioDTO.setCpf(txtCpf.getText());
        funcionarioDTO.setRg(txtRg.getText());
       
        JOptionPane.showMessageDialog(null, funcionarioCTR.gravarFuncionario(funcionarioDTO));
        
    }//GEN-LAST:event_btnGravarActionPerformed

